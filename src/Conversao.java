public class Conversao {

    public static void main (String args[]){

        int valor1 = 2348675;
        System.out.println(valor1);

        valor1 = Integer.valueOf(valor1);
        System.out.println(valor1);

        double valor1Double = 15.13;
        System.out.println(valor1Double);

        valor1Double = Double.valueOf(valor1Double);
        System.out.println(valor1Double);

    }
}
